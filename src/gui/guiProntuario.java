    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        Prontuario p = new Prontuario();
        p.setPaciente((Paciente)cboPaciente.getSelectedItem());
        p.setMedico((Medico)cboMedico.getSelectedItem());
        p.setDataConsulta(txtData.getText());
        p.setInformacao(txtInformacao.getText());
        new ProntuarioJpaController().create(p);
        txtId.setText(p.getId().toString());
        
    }//GEN-LAST:event_btnGravarActionPerformed

