    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        cboPaciente.removeAllItems();
        List l4 = new PacienteJpaController().findPacienteEntities();
        for (int i=0;i<l4.size();i++){
            //cboPaciente.addItem(i);
            cboPaciente.addItem(l4.get(i));
        }
        cboMedico.removeAllItems();
        List l5 = new MedicoJpaController().findMedicoEntities();
        for (int i=0;i<l5.size();i++){
            cboMedico.addItem(l5.get(i));
            
        }
    }//GEN-LAST:event_formWindowActivated

