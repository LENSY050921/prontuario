    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        Triagem t = new Triagem();
        t.setDataTriagem(txtData.getText());
        t.setPeso(txtPeso.getText());
        t.setGlicemia(txtGlicemia.getText());
        t.setCa(txtCa.getText());
        t.setPaciente((Paciente)cboPaciente.getSelectedItem());
        t.setFuncionario((Funcionario)cboFuncionario.getSelectedItem());
        
        new TriagemJpaController().create(t);
        txtId.setText(t.getId().toString());
        
    }//GEN-LAST:event_btnGravarActionPerformed

