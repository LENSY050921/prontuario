    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        cboPaciente.removeAllItems();
        List l6 = new PacienteJpaController().findPacienteEntities();
        for (int i=0;i<l6.size();i++){
            cboPaciente.addItem(l6.get(i));
            
        }
        cboFuncionario.removeAllItems();
        List l7 = new FuncionarioJpaController().findFuncionarioEntities();
        for (int i=0;i<l7.size();i++){
            cboFuncionario.addItem(l7.get(i));
        }
    }//GEN-LAST:event_formWindowActivated

