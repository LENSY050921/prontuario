    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        Agenda a = new Agenda();
        a.setDataAgenda(txtData.getText());
        a.setHora(txtHora.getText());
        a.setMedico((Medico)cboMedico.getSelectedItem());
        a.setPaciente((Paciente)cboPaciente.getSelectedItem());
        
        new AgendaJpaController().create(a);
        txtId.setText(a.getId().toString());
        
    }//GEN-LAST:event_btnGravarActionPerformed

