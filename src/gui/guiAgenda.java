    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        cboMedico.removeAllItems();
        List l2 = new MedicoJpaController().findMedicoEntities();
        for (int i=0;i<l2.size();i++){
            cboMedico.addItem(l2.get(i));
        }
        cboPaciente.removeAllItems();
        List l3 = new PacienteJpaController().findPacienteEntities();
        for (int i=0;i<l3.size();i++){
            cboPaciente.addItem(l3.get(i));
            
        }
    }//GEN-LAST:event_formWindowActivated

